# Age of Person
Age = 20

# Less Than 2
if Age < 2:
    print("This is a Baby.")

# Less Than 4 & More Than 2
elif Age < 4:
    print("This is a Toddler.")

# Less Than 13 & More Than 4
elif Age < 13:
    print("This is a Kid.")

# Less Than 20 & More Than 13
elif Age < 20:
    print("This is a Teenager.")

# Less Than 65 & More Than 20
elif Age < 65:
    print("This is an Adult.")

# 65 or More
else:
    print("This is an Elder.")
