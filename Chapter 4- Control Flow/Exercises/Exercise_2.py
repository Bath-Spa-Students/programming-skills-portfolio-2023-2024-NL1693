# Variable with Color Assigned
alien_color = 'green'

# If Alien is Green, Give 5 Points. If not, Give 10 Points.     # Version = If Block
if alien_color == 'green':
    print ("+5 Points Earned!")
else:
    print ("+10 Points Earned!")

# Variable with Color Assigned
alien_color = 'red'

# If Alien is Green, Give 5 Points. If not, Give 10 Points.     # Version = Else Block
if alien_color == 'green':
    print ("+5 Points Earned!")
else:
    print ("+10 Points Earned!")